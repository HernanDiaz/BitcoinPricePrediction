def extract_mlp_attention_weights(config, df, cv, results_dir: Path):
    """Load saved MLPDualEncoder models, collect cross-attention weights."""
    from src.models.mlp_dual_encoder import MLPDualEncoderModel

    seed = config["project"]["random_seed"]
    models_dir = results_dir / "models"
    exp_id = "MLPDualEncoder_G3"
    target_col = config["data"]["target_column"]

    g3_cols = [c for c in df.columns if c != target_col]
    tech_cols, onchain_cols = get_dual_encoder_splits(list(df.columns))
    tech_g3    = [c for c in tech_cols    if c in g3_cols]
    onchain_g3 = [c for c in onchain_cols if c in g3_cols]
    tech_idx    = [g3_cols.index(c) for c in tech_g3]
    onchain_idx = [g3_cols.index(c) for c in onchain_g3]

    all_tech_w, all_onchain_w = [], []

    for fold in cv:
        model_path = models_dir / f"{exp_id}_{fold.label}.pt"
        if not model_path.exists():
            logger.warning(f"  Model not found: {model_path.name}")
            continue

        df_train, df_test = fold.split(df)
        prep = FoldPreprocessor(sequence_length=1)
        prep.fit_transform(df_train, g3_cols, target_col)
        Xf_te, _, _, _ = prep.transform(df_test, g3_cols, target_col)

        Xt_te = Xf_te[:, tech_idx]
        Xo_te = Xf_te[:, onchain_idx]

        model = MLPDualEncoderModel(
            config,
            n_technical=len(tech_g3),
            n_onchain=len(onchain_g3),
            random_seed=seed,
        )
        model.load(model_path)
        model.predict_proba((Xt_te, Xo_te))

        tw, ow = model.get_attention_weights()  # (n, K, K) after MHA avg
        # MHA with batch_first returns attn_output_weights averaged over heads: (B, Ltgt, Lsrc)
        # Shape: (n_samples, K, K) — mean over samples
        all_tech_w.append(tw.mean(axis=0))
        all_onchain_w.append(ow.mean(axis=0))
        logger.info(f"  [{fold.label}] tech_attn={tw.shape}, onchain_attn={ow.shape}")

    if not all_tech_w:
        return None, None

    mean_tech    = np.stack(all_tech_w).mean(axis=0)
    mean_onchain = np.stack(all_onchain_w).mean(axis=0)

    # If heads not averaged by PyTorch (shape: n_heads, K, K), collapse
    if mean_tech.ndim == 3:
        mean_tech    = mean_tech.mean(axis=0)
        mean_onchain = mean_onchain.mean(axis=0)

    return mean_tech, mean_onchain


def plot_mlp_attention(tech_w: np.ndarray, onchain_w: np.ndarray, output_dir: Path) -> None:
    K = tech_w.shape[-1]
    tick_labels = [f"K{i}" for i in range(K)]

    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    for ax, w, title in zip(
        axes,
        [tech_w, onchain_w],
        ["Technical Context → On-Chain Attention", "On-Chain Context → Technical Attention"],
    ):
        sns.heatmap(
            w, ax=ax, cmap="viridis", cbar=True,
            xticklabels=tick_labels, yticklabels=tick_labels,
            linewidths=0.3,
        )
        ax.set_title(title, fontsize=11)
        ax.set_xlabel("Key Context Token")
        ax.set_ylabel("Query Context Token")

    plt.suptitle(
        "MLP Dual Encoder Cross-Attention Weights (Mean over All Test Folds)",
        y=1.02, fontsize=12,
    )
    plt.tight_layout()
    _save(fig, output_dir, "fig9_mlp_attention_weights")


