def main():
    config = load_config(ROOT / "config.yaml")
    results_dir = ROOT / config["paths"]["results"]
    metrics_dir = results_dir / "metrics"
    plots_dir   = results_dir / "plots"

    df = load_dataset(config)
    df = impute_missing(df)
    cv = WalkForwardCV(config)

    preds      = load_predictions(metrics_dir)
    summary_df = load_summaries(metrics_dir)

    # ── Fig 1: Walk-forward diagram ────────────────────────────────
    logger.info("\n[Fig 1] Walk-forward diagram")
    plot_walk_forward_diagram(list(cv), plots_dir)

    # ── Fig 2: Dataset overview ────────────────────────────────────
    logger.info("[Fig 2] Dataset overview")
    plot_dataset_overview(df, config["data"]["target_column"], plots_dir)

    # ── Fig 3: Ablation heatmap ────────────────────────────────────
    logger.info("[Fig 3] Ablation heatmap")
    plot_ablation_heatmap(summary_df, plots_dir)

    # ── Fig 4: ROC curves (all 14 experiments) ─────────────────────
    logger.info("[Fig 4] ROC curves")
    roc_data = {}
    for exp_id, p in preds.items():
        if p["y_proba_all"] is None:
            continue
        row = summary_df[summary_df["experiment_id"] == exp_id]
        if row.empty:
            continue
        roc_data[exp_id] = {
            "y_true_all":  p["y_true_all"],
            "y_proba_all": p["y_proba_all"],
            "model": row.iloc[0]["model"],
            "group": row.iloc[0]["group"],
        }
    # Add MLPDualEncoder to the MODEL_COLORS mapping at plot time via monkey-patch
    import src.visualization.plots as plots_module
    if "MLPDualEncoder" not in plots_module.MODEL_COLORS:
        plots_module.MODEL_COLORS["MLPDualEncoder"] = plots_module.PALETTE[4]
    plot_roc_curves(roc_data, plots_dir)

    # ── Fig 8: Confusion matrices ──────────────────────────────────
    logger.info("[Fig 8] Confusion matrices")
    best_models = {
        "G0": "XGBoost_G0",
        "G1": "RandomForest_G1",
        "G2": "XGBoost_G2",
        "G3": "XGBoost_G3",
    }
    cms = {}
    for group, exp_id in best_models.items():
        if exp_id not in preds:
            logger.warning(f"  Predictions not found for {exp_id}")
            continue
        y_true = preds[exp_id]["y_true_all"]
        y_pred = preds[exp_id]["y_pred_all"]
        cms[exp_id] = confusion_matrix(y_true, y_pred)
    if cms:
        plot_confusion_matrices(cms, plots_dir)

    # ── Fig 9: MLPDualEncoder attention weights ────────────────────
    logger.info("[Fig 9] MLPDualEncoder attention weights")
    tech_w, onchain_w = extract_mlp_attention_weights(config, df, cv, results_dir)
    if tech_w is not None:
        plot_mlp_attention(tech_w, onchain_w, plots_dir)
    else:
        logger.warning("  Could not extract attention weights (no saved models found)")

    logger.info(f"\n{'='*60}")
    logger.info(f"All figures saved to: {plots_dir}")
    logger.info(f"{'='*60}")


if __name__ == "__main__":
    main()
