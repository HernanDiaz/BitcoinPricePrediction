def _save(fig, output_dir: Path, name: str) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_dir / f"{name}.pdf")
    fig.savefig(output_dir / f"{name}.png", dpi=300)
    plt.close(fig)
    logger.info(f"  Saved: {name}.pdf/.png")


def load_predictions(metrics_dir: Path) -> dict:
    preds = {}
    for y_true_path in sorted(metrics_dir.glob("*_y_true.npy")):
        exp_id = y_true_path.stem.replace("_y_true", "")
        y_pred_path  = metrics_dir / f"{exp_id}_y_pred.npy"
        y_proba_path = metrics_dir / f"{exp_id}_y_proba.npy"
        if not y_pred_path.exists():
            continue
        preds[exp_id] = {
            "y_true_all":  np.load(y_true_path),
            "y_pred_all":  np.load(y_pred_path),
            "y_proba_all": np.load(y_proba_path) if y_proba_path.exists() else None,
        }
    return preds


def load_summaries(metrics_dir: Path) -> pd.DataFrame:
    rows = []
    for f in sorted(metrics_dir.glob("*_summary.json")):
        with open(f) as fp:
            rows.append(json.load(fp))
    return pd.DataFrame(rows)


